const getInitials = (text?: string) => {
  if (!text) return "EC";
  const parts = text.trim().split(" ");
  const first = parts[0]?.[0] ?? "";
  const second = parts[1]?.[0] ?? "";
  return (first + second || first || "EC").toUpperCase();
};

